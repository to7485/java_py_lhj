package ch01;

//클래스
//자바 프로그램의 기본 구성 단위
//자바에서 모든 코드는 반드시 클래스의 블록({ })안에 있어야 한다.
//클래스는 하나의 파일(.java)과 1:1 관계이며, 클래스의 이름과
//파일의 이름은 같아야 한다.
public class Hello {
	//main 메서드
	//자바 프로그램을 실행할 때 jvm이 main메서드를 실행한다.
	//main메서드는 형식이 정해져있고, 변경하면 실행되지 않는다.
	public static void main(String[] args) {
		//js에서 화면에 출력하려면 console.log(내용);
		//println() : ()안에 있는 내용을 콘솔에 출력하는 기능
		System.out.println("Hello, Java!");
		
		
		
	}
}






